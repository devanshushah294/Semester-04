# some basic datatypes in python
a = 20;
print((type(a)));

b = 30.00;
print(type(b));

c = True;
print(type(c));

d = {2,3,4};
print(type(d));

e = (3,4,5);
print(type(e));

f = [4,5,6];
print(type(f));

g = 'g';
print(type(g));

sstr = "str";
print(type(sstr));

#outputs
#<class 'int'>
# <class 'float'>
# <class 'bool'>
# <class 'set'>
# <class 'tuple'>
